public class Int {
	public static void main(String[] args) {
		
		
			//运算顺序从左到右
		System.out.println("100 + 2");  //100 + 2
		System.out.println("100" + 2);  //1002
		System.out.println("hello" + 2);  //hello2
		System.out.println(2 + "hello");  //2hello
		System.out.println(100 + 2);  //102
		System.out.println("hello" + 100 + 3); //hello1003
	}
}