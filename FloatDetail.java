public class FloatDetail {
	public static void main(String[] args) {
		//浮点型（小数）
			//float四个字节
		float a = 1.1F;
			//double八个字节
		double b = 1.2;
		//double b = 1.1F;		可放 但浪费空间（浪费字节）
		
		
		double num5 = 0.023;
		System.out.println(num5);
		
		
		System.out.println(5.21e2);		//小数e2 小数乘十的二次方
		System.out.println(521E-2);
		
		
	}
}