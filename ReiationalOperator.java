public class ReiationalOperator {
	public static void main(String[] args) {
	int a = 9;
	int b = 8;//开发中不可用a b a1 b1命名
	System.out.println(a > b);//ture
	System.out.println(a >= b);//ture
	System.out.println(a <= b);//false
	System.out.println(a < b);//false
	System.out.println(a == b);//false
	System.out.println(a != b);//ture
	boolean flag = a > b;
	System.out.println("flag=" + flag);
		
	}
}	